package co.grandcircus;

public class Algorithms {
	public static void main(String[] args) {
		/*
		 * Algorithms do not represent complete programs, but rather the core of the
		 * problem. They can be expressed in pseudo-code or flowcharts.
		 */
		
		int x;
		int y;
		int z;
		
		x = 5;
		y = 2;
		
		z = x + y;
		
		System.out.println(z);
		
	}
}
